  `SUBJECT` VARCHAR(600) NULL,
  `DEALER_CD` CHAR(6) NULL,
  `CITY` VARCHAR(200) NULL,
  `PROVINCE` VARCHAR(200) NULL,
  `REGION` VARCHAR(40) NULL,
  `FRAME_NO` CHAR(17) NULL,
  `CAR_MODEL` VARCHAR(40) NULL,
  `CAR_TYPE` CHAR(3) NULL,
  `MODEL_YEAR` VARCHAR(4) NULL,
  `NUMBER_DAYS` INT NULL,
  `PRODUCTION_DATE` VARCHAR(8) NULL,
