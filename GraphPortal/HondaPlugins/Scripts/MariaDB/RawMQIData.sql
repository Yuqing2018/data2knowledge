  `FAULT_DATE` TIMESTAMP NULL,
  `MILE_AGE` INT NULL,
  `SYMPTOM_DESC` VARCHAR(3000) NULL,
  `MAINTENANCE_PROCESS` VARCHAR(3000) NULL,
